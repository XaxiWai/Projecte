package tpv;

public class LiniaTiquet {
	private int nLinia;
	private int quantitat;
	
	//constructor per defecte
	public LiniaTiquet() {
		
	}

	//getters i setters
	public int getnLinia() {
		return nLinia;
	}

	public void setnLinia(int nLinia) {
		this.nLinia = nLinia;
	}

	public int getQuantitat() {
		return quantitat;
	}

	public void setQuantitat(int quantitat) {
		this.quantitat = quantitat;
	}

	
}
